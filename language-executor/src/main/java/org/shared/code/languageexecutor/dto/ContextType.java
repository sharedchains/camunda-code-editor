package org.shared.code.languageexecutor.dto;

/**
 * The enum Context type.
 */
public enum ContextType {
    /**
     * Boolean context type.
     */
    BOOLEAN,
    /**
     * Bytes context type.
     */
    BYTES,
    /**
     * String context type.
     */
    STRING,
    /**
     * Short context type.
     */
    SHORT,
    /**
     * Double context type.
     */
    DOUBLE,
    /**
     * Integer context type.
     */
    INTEGER,
    /**
     * Long context type.
     */
    LONG,
    /**
     * Date context type.
     */
    DATE,
    /**
     * Datetime context type.
     */
    DATETIME,
    /**
     * Json context type.
     */
    JSON,
    /**
     * Xml context type.
     */
    XML
}
